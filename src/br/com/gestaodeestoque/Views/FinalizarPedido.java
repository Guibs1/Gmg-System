    public void listarClientes() {
        ClientesDAO clientesdao = new ClientesDAO();
        List<ClientesModelo> listadeclientes = clientesdao.listarClientes();
        cmbClientes.removeAllItems();
        cmbClientes.addItem("");
        for (ClientesModelo clientesmodelo : listadeclientes) {
            cmbClientes.addItem(clientesmodelo);
        }
    }

    public PedidosModelo cadastrarPedido() {
        try {
            PedidosModelo pedidosmodelo = new PedidosModelo();
            ClientesModelo clientemodelo = new ClientesModelo();

            clientemodelo = (ClientesModelo) cmbClientes.getSelectedItem();
            pedidosmodelo.setCliente(clientemodelo);
            pedidosmodelo.setFuncionariomodelo(funcionariomodelo);
            SimpleDateFormat formatador = new SimpleDateFormat("yyyy-MM-dd");
//            String dataformatada = formatador.format(txtData.getDate());
//            pedidosmodelo.setData(dataformatada);
            pedidosmodelo.setHorario_retirada(txtHoraRetirada.getText());
            pedidosmodelo.setTotal(this.total);
            if(!txtDesconto.getText().equals(""))
            {
            pedidosmodelo.setDesconto(Double.parseDouble(txtDesconto.getText()));
            }else{pedidosmodelo.setDesconto(0);}
            PedidosDAO pedidosdao = new PedidosDAO();
//            this.pedidosmodelo = pedidosmodelo;
            this.pedidosmodelo = pedidosdao.cadastrarPedido(pedidosmodelo);
            
            JOptionPane.showMessageDialog(null, "Pedido cadastrado com sucesso.");
            return this.pedidosmodelo; 
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

